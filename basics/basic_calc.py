num1 = float(input("Enter number 1: "))
num2 = float(input("Enter number 2: "))
sum = num1 + num2
mult = num1 * num2
sub = num1 - num2
div = num1 / num2
mod = num1 % num2
pow = num1 ** num2
floor_div = num1 // num2
print(
    "Sum = " + str(sum) + "\n"
    "Subtract = "
    + str(sub)
    + "\n"
    + "Multiply = "
    + str(mult)
    + "\n"
    + "Divide = "
    + str(div)
    + "\nModulus = "
    + str(mod)
    + "\nPower = "
    + str(pow)
    + "\nFloor Division = "
    + str(floor_div)
)
